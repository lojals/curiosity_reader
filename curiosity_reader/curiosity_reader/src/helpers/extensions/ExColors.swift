import Foundation
import UIKit

extension UIColor {
    class func themeMain() -> UIColor {
        return UIColor(red:0.000, green:0.686, blue:0.725, alpha: 1)
    }
    
    class func themeGreyLight() -> UIColor {
        return UIColor(red:0.965, green:0.965, blue:0.965, alpha: 1)
    }
    
    class func themeGreyMedium() -> UIColor {
        return UIColor(red:0.882, green:0.882, blue:0.882, alpha: 1)
    }
    
    class func themeGreyDark() -> UIColor {
        return UIColor(red:0.663, green:0.663, blue:0.663, alpha: 1)
    }
}